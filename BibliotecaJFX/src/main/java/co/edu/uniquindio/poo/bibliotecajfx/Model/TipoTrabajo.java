package co.edu.uniquindio.poo.bibliotecajfx.Model;

/**
 * Enumera los tipos de trabajo o cargos que puede tener un usuario en la biblioteca.
 *
 * <ul>
 *   <li>{@link #ADMINISTRADOR}: Usuario con rol de administrador.</li>
 *   <li>{@link #BIBLIOTECARIO}: Usuario con rol de bibliotecario.</li>
 * </ul>
 *
 * @author
 */
public enum TipoTrabajo {
    /**
     * Rol de administrador.
     */
    ADMINISTRADOR,

    /**
     * Rol de bibliotecario.
     */
    BIBLIOTECARIO;
}
